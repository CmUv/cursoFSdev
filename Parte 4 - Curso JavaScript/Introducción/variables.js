// Variable declarada correctamente siguiendo convenciones de nomenclatura.
// Se usa camelCase para nombres de variables (primera palabra en minúscula, 
// las siguientes con inicial mayúscula).
let cajaDeAndy = 'Woody';
console.log(cajaDeAndy);  // Muestra el contenido de 'cajaDeAndy' en la consola.

// Lo NO permitido 
// Estas variables no cumplen con las buenas prácticas de nomenclatura:

// let c = 'Woody'; // ❌ Muy corto y poco descriptivo, no indica el propósito de la variable.
// let cda = 'Woody'; // ❌ Abreviaciones que no aportan claridad al significado.
// let pcAndy = 'Woody'; // ❌ Uso ambiguo de acrónimos. Difícil de entender sin contexto adicional.

// Lo permitido
// Variables nombradas correctamente usando camelCase y descripciones claras:

